    public void showdata() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        try {
            try {
                int ml = model.getRowCount();
                for (int mh = 0; mh <= ml; ++mh) {
                    model.removeRow(0);
                }
            } catch (Exception e) {
            }
            try {
                Connection con = ConnectionProvider.getConnection();
                Statement stmt = con.createStatement();
                String query = "SELECT * FROM part_master";
                ResultSet rs = stmt.executeQuery(query);
                while (rs.next()) {
                    model.addRow(new Object[]{rs.getString("ID"), rs.getString("partname"), rs.getString("partcost"), rs.getString("partsalecost"), rs.getString("qty"), rs.getString("purchaseDate"), rs.getString("warranty"), rs.getString("Category")});
                }

                rs.close();
                stmt.close();
                con.close();
            } catch (Exception ex) {
                System.out.println("error " + ex.toString());
            }
            jTable2.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error in connectivity");
        }         // TODO a
    }

