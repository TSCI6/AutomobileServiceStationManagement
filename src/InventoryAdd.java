    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed

        if ("".equalsIgnoreCase(txtID.getText().trim())) {
            return;
        }
        try {
            Connection con = ConnectionProvider.getConnection();
            Statement stmt = con.createStatement();
            String query = "SELECT * FROM part_master where ID='" + txtID.getText().trim() + "'";
            ResultSet rs = stmt.executeQuery(query);
            if (rs.next()) {
                txtPartName.setText(rs.getString("partname"));
                txtPartCost.setText(rs.getString("partcost"));
                txtPartSalesPrice.setText(rs.getString("partsalecost"));
                txtQty.setText(rs.getString("qty"));
                txtpurchageDate.setText(rs.getString("purchaseDate"));
                txtWarranty.setText(rs.getString("warranty"));
                combCategory.setSelectedItem(rs.getString("Category"));
                // combCategory.getSelectedItem(rs.getString("Category"));
            }
            rs.close();
            stmt.close();
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(InventoryAdd.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btn_SearchActionPerformed


